user_history = {}


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Вітаю 👋\n\n"
        "Мене звати Андрій.\n"
        "Я менеджер компанії PIXORA.\n\n"
        "Як до вас звертатись?"
    )


async def chat(update: Update, context: ContextTypes.DEFAULT_TYPE):

    user_id = str(update.effective_user.id)
    user_message = update.message.text

    if user_id not in user_history:
        user_history[user_id] = []

    user_history[user_id].append(
        {
            "role": "user",
            "content": user_message
        }
    )

    messages = [
        {
            "role": "system",
            "content": SYSTEM_PROMPT
        }
    ] + user_history[user_id]

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=messages,
        temperature=0.8
    )

    answer = response.choices[0].message.content
    print(answer)
    if "[PIXORA_LEAD_READY]" in answer:

        await context.bot.send_message(
            chat_id=499657192,
            text=answer
        )
        print("LEAD SENT")
        clean_answer = (
            "Дякую за надану інформацію.\n\n"
            "Я вже сформував попередній опис вашого проєкту та передам його нашому профільному спеціалісту.\n\n"
            "З вами зв'яжеться Сергій, який надалі буде супроводжувати проєкт та допоможе узгодити всі деталі безпосередньо.\n\n"
            "Дякуємо, що звернулися до компанії PIXORA."
        )

    else:
        clean_answer = answer

    user_history[user_id].append(
        {
            "role": "assistant",
            "content": clean_answer
        }
    )

    await update.message.reply_text(clean_answer)
    
def main():

    app = ApplicationBuilder().token(BOT_TOKEN).build()

    app.add_handler(
        CommandHandler(
            "start",
            start
        )
    )

    app.add_handler(
        MessageHandler(
            filters.TEXT & ~filters.COMMAND,
            chat
        )
    )

    print("PIXORA Manager started")

    app.run_polling()


if __name__ == "__main__":
    main()
